-- Затем добавляем новые продукты на все склады
WITH new_products AS (
    SELECT product_id 
    FROM products 
    WHERE product_id > 10 -- Предполагаем, что первые 10 товаров уже существуют в базе
),
all_warehouses AS (
    SELECT warehouse_id 
    FROM warehouses
)
INSERT INTO stock (product_id, warehouse_id, quantity, last_restocked)
SELECT 
    np.product_id,
    aw.warehouse_id,
    -- Генерируем случайное количество товаров в диапазоне от 10 до 100
    (RANDOM() * 90 + 10)::INTEGER,
    CURRENT_DATE
FROM 
    new_products np
CROSS JOIN 
    all_warehouses aw
WHERE NOT EXISTS (
    SELECT 1 
    FROM stock 
    WHERE product_id = np.product_id AND warehouse_id = aw.warehouse_id
);

-- Обновляем существующие записи, если нужно
UPDATE stock
SET 
    quantity = quantity + FLOOR(RANDOM() * 50 + 10)::INTEGER,
    last_restocked = CURRENT_DATE,
    updated_at = CURRENT_TIMESTAMP
WHERE 
    product_id IN (SELECT product_id FROM products WHERE product_id > 10)
    AND EXISTS (
        SELECT 1 
        FROM stock s2 
        WHERE s2.product_id = stock.product_id AND s2.warehouse_id = stock.warehouse_id
    );

-- Проверка распределения товаров по категориям
SELECT 
    category, 
    COUNT(*) as total_products,
    STRING_AGG(product_name, ', ' ORDER BY product_name) as product_names
FROM 
    products 
GROUP BY 
    category 
ORDER BY 
    category;

-- Проверка распределения товаров по складам
SELECT 
    w.warehouse_name, 
    COUNT(s.stock_id) as total_items,
    SUM(s.quantity) as total_quantity
FROM 
    warehouses w
LEFT JOIN 
    stock s ON w.warehouse_id = s.warehouse_id
GROUP BY 
    w.warehouse_id, w.warehouse_name
ORDER BY 
    w.warehouse_id;

-- Проверка наличия товаров всех категорий на всех складах
SELECT 
    w.warehouse_name,
    p.category,
    COUNT(s.stock_id) as items_count,
    SUM(s.quantity) as total_quantity
FROM 
    warehouses w
CROSS JOIN (
    SELECT DISTINCT category FROM products
) p
LEFT JOIN 
    products pr ON p.category = pr.category
LEFT JOIN 
    stock s ON pr.product_id = s.product_id AND w.warehouse_id = s.warehouse_id
GROUP BY 
    w.warehouse_name, p.category
ORDER BY 
    w.warehouse_name, p.category; 